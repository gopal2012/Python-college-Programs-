# Write a Python program that demonstrates the use of arithmetic operators by performing basic operations on two numbers.

# Demonstrating arithmetic operators
a = 15
b = 4

# Addition
addition = a + b
# Subtraction
subtraction = a - b
# Multiplication
multiplication = a * b
# Division
division = a / b
# Modulus
modulus = a % b
# Exponentiation
exponentiation = a ** b
# Floor division
floor_division = a // b

print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Modulus:", modulus)
print("Exponentiation:", exponentiation)
print("Floor Division:", floor_division)
