# Write a Python program to create variables of different data types (integer, float, string, and boolean) and print their types.


# Creating variables of different data types
integer_var = 10
float_var = 3.14
string_var = "Hello, Python!"
boolean_var = True

# Printing the type of each variable
print("Type of integer_var:", type(integer_var))
print("Type of float_var:", type(float_var))
print("Type of string_var:", type(string_var))
print("Type of boolean_var:", type(boolean_var))
